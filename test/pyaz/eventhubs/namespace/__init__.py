import json, subprocess
from ... pyaz_utils import get_cli_name, get_params


def create(resource_group, name, location=None, tags=None, sku=None, capacity=None, enable_auto_inflate=None, maximum_throughput_units=None, enable_kafka=None, default_action=None, assign_identity=None, zone_redundant=None, cluster_arm_id=None, enable_trusted_service_access=None):
    params = get_params(locals())   
    command = "az eventhubs namespace create " + params
    print(command)
    output = subprocess.run(command, shell=True, stdout=subprocess.PIPE, stderr=subprocess.PIPE)
    stdout =  output.stdout.decode("utf-8")
    stderr = output.stderr.decode("utf-8")
    if stdout:
        return json.loads(stdout)
        print(stdout)
    else:
        raise Exception(stderr)
        print(stderr)  

