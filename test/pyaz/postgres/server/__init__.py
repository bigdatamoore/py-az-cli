import json, subprocess
from ... pyaz_utils import get_cli_name, get_params


def create(resource_group=None, name=None, sku_name=None, location=None, admin_user=None, admin_password=None, backup_retention=None, geo_redundant_backup=None, ssl_enforcement=None, storage_size=None, tags=None, version=None, auto_grow=None, assign_identity=None, public_network_access=None, infrastructure_encryption=None, minimal_tls_version=None):
    params = get_params(locals())   
    command = "az postgres server create " + params
    print(command)
    output = subprocess.run(command, shell=True, stdout=subprocess.PIPE, stderr=subprocess.PIPE)
    stdout =  output.stdout.decode("utf-8")
    stderr = output.stderr.decode("utf-8")
    if stdout:
        return json.loads(stdout)
        print(stdout)
    else:
        raise Exception(stderr)
        print(stderr)  

