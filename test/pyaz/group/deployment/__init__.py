import json, subprocess
from ... pyaz_utils import get_cli_name, get_params


def create(resource_group, template_file=None, template_uri=None, name=None, parameters=None, mode=None, rollback_on_error=None, handle_extended_json_format=None, aux_subs=None, aux_tenants=None, no_prompt=None, no_wait=None):
    params = get_params(locals())   
    command = "az group deployment create " + params
    print(command)
    output = subprocess.run(command, shell=True, stdout=subprocess.PIPE, stderr=subprocess.PIPE)
    stdout =  output.stdout.decode("utf-8")
    stderr = output.stderr.decode("utf-8")
    if stdout:
        return json.loads(stdout)
        print(stdout)
    else:
        raise Exception(stderr)
        print(stderr)  

