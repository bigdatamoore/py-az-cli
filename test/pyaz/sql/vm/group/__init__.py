import json, subprocess
from .... pyaz_utils import get_cli_name, get_params


def update(resource_group, name, domain_fqdn=None, operator_acc=None, service_acc=None, storage_account=None, sa_key=None, bootstrap_acc=None, fsw_path=None, ou_path=None, tags=None, set=None, add=None, remove=None, force_string=None):
    params = get_params(locals())   
    command = "az sql vm group update " + params
    print(command)
    output = subprocess.run(command, shell=True, stdout=subprocess.PIPE, stderr=subprocess.PIPE)
    stdout =  output.stdout.decode("utf-8")
    stderr = output.stderr.decode("utf-8")
    if stdout:
        return json.loads(stdout)
        print(stdout)
    else:
        raise Exception(stderr)
        print(stderr)  

