import json, subprocess
from .. pyaz_utils import get_cli_name, get_params


def create(resource_group, name, location=None, compute_vm_size=None, compute_count=None, aad_client_app_id=None, aad_client_app_secret=None, aad_tenant_id=None, vnet_prefix=None, subnet_prefix=None, vnet_peer=None, tags=None, workspace_id=None, customer_admin_group_id=None, no_wait=None):
    params = get_params(locals())   
    command = "az openshift create " + params
    print(command)
    output = subprocess.run(command, shell=True, stdout=subprocess.PIPE, stderr=subprocess.PIPE)
    stdout =  output.stdout.decode("utf-8")
    stderr = output.stderr.decode("utf-8")
    if stdout:
        return json.loads(stdout)
        print(stdout)
    else:
        raise Exception(stderr)
        print(stderr)  

