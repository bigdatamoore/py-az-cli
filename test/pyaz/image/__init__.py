import json, subprocess
from .. pyaz_utils import get_cli_name, get_params


def create(resource_group, name, source, os_type=None, data_disk_sources=None, location=None, __SOURCE_VIRTUAL_MACHINE=None, storage_sku=None, hyper_v_generation=None, __OS_BLOB_URI=None, __DATA_BLOB_URIS=None, __OS_SNAPSHOT=None, __DATA_SNAPSHOTS=None, __OS_DISK=None, os_disk_caching=None, __DATA_DISKS=None, data_disk_caching=None, tags=None, zone_resilient=None, edge_zone=None):
    params = get_params(locals())   
    command = "az image create " + params
    print(command)
    output = subprocess.run(command, shell=True, stdout=subprocess.PIPE, stderr=subprocess.PIPE)
    stdout =  output.stdout.decode("utf-8")
    stderr = output.stderr.decode("utf-8")
    if stdout:
        return json.loads(stdout)
        print(stdout)
    else:
        raise Exception(stderr)
        print(stderr)  

