import json, subprocess
from ... pyaz_utils import get_cli_name, get_params


def create(resource_group, name, location=None, __SOURCE_DICT=None, __SCRIPTS_LIST=None, __DESTINATIONS_LISTS=None, build_timeout=None, tags=None, image_source=None, scripts=None, checksum=None, managed_image_destinations=None, shared_image_destinations=None, image_template=None, identity=None, vm_size=None, os_disk_size=None, vnet=None, subnet=None, no_wait=None):
    params = get_params(locals())   
    command = "az image builder create " + params
    print(command)
    output = subprocess.run(command, shell=True, stdout=subprocess.PIPE, stderr=subprocess.PIPE)
    stdout =  output.stdout.decode("utf-8")
    stderr = output.stderr.decode("utf-8")
    if stdout:
        return json.loads(stdout)
        print(stdout)
    else:
        raise Exception(stderr)
        print(stderr)  

