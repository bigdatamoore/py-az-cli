import json, subprocess
from ... pyaz_utils import get_cli_name, get_params


def create(resource_group, name, subnet, private_connection_resource_id, connection_name, group_id=None, vnet_name=None, tags=None, location=None, request_message=None, manual_request=None, edge_zone=None):
    params = get_params(locals())   
    command = "az network private-endpoint create " + params
    print(command)
    output = subprocess.run(command, shell=True, stdout=subprocess.PIPE, stderr=subprocess.PIPE)
    stdout =  output.stdout.decode("utf-8")
    stderr = output.stderr.decode("utf-8")
    if stdout:
        return json.loads(stdout)
        print(stdout)
    else:
        raise Exception(stderr)
        print(stderr)  

